import SwiftUI

extension Color {
    enum Brand {
        // Pokemon base colors
        static let blue = Color(hex: "3466AF")
        static let darkBlue = Color(hex: "21386E")
        static let navyBlue = Color(hex: "1D2C5E")
        static let yellow = Color(hex: "FFCB05")
        static let darkYellow = Color(hex: "C7A008")


        static let clear = Color.clear
        static let black = Color.black
        static let white = Color.white
        static let primary = yellow
        static let secondary = blue
    }
}
